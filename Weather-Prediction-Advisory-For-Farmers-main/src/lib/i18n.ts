export function t(lang: Language, key: string, vars?: Record<string, string | number>): string {
  const template = messages[lang][key] ?? messages.en[key] ?? key
  if (!vars) return template
  return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), template)
}

export function conditionLabel(lang: Language, c: WeatherCondition): string {
  return t(lang, `condition.${c}`)
}

export function langLabel(lang: Language): string {
  return t(lang, `lang.${lang}`)
}

export function localeForLanguage(lang: Language): string {
  if (lang === 'hi') return 'hi-IN'
  if (lang === 'mr') return 'mr-IN'
  return 'en-IN'
}

